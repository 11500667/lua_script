local request_method = ngx.var.request_method
local args = nil
if "GET" == request_method then
    args = ngx.req.get_uri_args()
else
    ngx.req.read_body()
    args = ngx.req.get_post_args()
end

--学科ID
local subject_id = tostring(args["scheme_id"])
--判断是否有资源类型参数
if subject_id == "nil" then
    ngx.say("{\"success\":\"false\",\"info\":\"参数错误！\"}")    
    return
end

local res = cache:get("mediatype_scheme_"..subject_id)
if tostring(res) == "userdata: NULL" then
    ngx.say("{\"success\":true,\"mt_list\":[]}")
else
    ngx.say("{\"success\":true,\"mt_list\":"..res.."}")
end


